     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(StoreExpenses $request, ExpenseReport $expenseReport)
    {

        $validate = $request->validated();
        $expense = new Expense();
        $expense->description = $validate['description'];
        $expense->amount = $validate['amount'];
        $expense->expense_report_id = $expenseReport->id;
        $expense->save();

        return redirect('/expense_reports/' . $expenseReport->id);
        // dd($request->all());
    }

